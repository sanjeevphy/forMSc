#include <stdio.h>
int main(){
  int i; // normal integer
  short S; // short integer
  long L; // long integer
  unsigned U;
  float f;
  double d; // double precision float
  char c;
  
  /* Reading and writing different datatypes */
  printf("Give the values of different variables:\nGive and integer: ");
  scanf("%d",&i);
  printf("give a short int: "); scanf("%hd",&S);
  printf("give a long int: "); scanf("%ld",&L);
  printf("give a unsigned int: "); scanf("%u",&U);
  printf("give a float number: "); scanf("%f",&f);
  printf("give a double precision float: "); scanf("%lf",&d);
  printf("give a charactor: "); do scanf("%c",&c);
  while(c=='\n' || c=='\t' || c==' '); // to take only visual characters
  printf("given int is %d\ngiven short is %hd\ngiven long is%ld\ngiven unsigned is %u\ngiven float is %f\ngiven double is %lf\ngiven char is %c\n\n", i,S,L,U,f,d,c);
  
  /* Assigning new values */
  i=123456789;
  L=123456789;
  U=123456789;
  S=1244;
  f=15.56;  // note the warning
  f=75.89f; // no warning here.
  d=15.345678;
  c='x';
  
  /* see how printf handles them.*/
  //Some compilers take sizeof() as unsigned long int.
  printf("An int: %d and size is %d and another way %d\n",i,sizeof(int),sizeof(i));
  printf("A long: %d and size is %d and another way %d\n",L,sizeof(long), sizeof(L));
  printf("A short: %d and size is %d\n",S,sizeof(short));
  printf("A unsigned: %d and size is %d\n",U,sizeof(unsigned));
  printf("A float: %f and size is %d\n",f,sizeof(float));
  printf("A double: %f and size is %d\n",d,sizeof(double));
  printf("A char: %c and size is %d\n",c,sizeof(char));
  //If your compiler gives you warning then please change %d by %ld for sizeof()

  /* some other data types:
     Data types:             printf:       scanf:
     long double             %Lf           %Lf
     double                  %f            %lf
     float                   %f            %f
     unsigned long int       %lu           %lu
     long int                %ld           %ld
     unsigned int            %u            %u
     int                     %d            %d
     unsigned short          %h            %hu
     short                   %h            %hd
     char                    %c            %c

     some people uses "signed char" and "unsigned char" also
     because in c-language char is just a int number.
  */
  
  /* Some special characters
     Escape Sequence                Meaning
     \a                            alarm ( bell )
     \b                            Backspace
     \f                            Form feed ( clears screen )
     \n                            New line
     \r                            Carriage return
     \t                            Horizontal tab
     \v                            Vertical tab
     \\                            Backslash
     \?                            Question mark
     \'                            Single quote
     \"                            Double quote
     \0                            Numerical zero ( null byte )
   */
  
  int ij;
  for (ij=0; ij<40; ij++) {
    i *=2;
    L *=2;
    S *=2;
    U *=2;
    f *=110;
    d *=111111210;
    printf("il %d %d %ld %hd %u %f %f\n",ij,i,L,S,U,f,d);
    printf("il %d %d %ld %hd %u %e %e\n",ij,i,L,S,U,f,d);
  }
  printf("\n\nL*1000: %ld\n", L);

  return 0;
}


